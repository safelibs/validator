unsafe extern "C" fn progress_monitor(mut cinfo: j_common_ptr) {
    let mut prog: cd_progress_ptr = (*cinfo).progress as cd_progress_ptr;
    if (*prog).max_scans != 0 as JDIMENSION && (*cinfo).is_decompressor != 0 {
        let mut scan_no: ::core::ffi::c_int = (*(cinfo as j_decompress_ptr)).input_scan_number;
        if scan_no > (*prog).max_scans as ::core::ffi::c_int {
            fprintf(
                stderr,
                b"Scan number %d exceeds maximum scans (%u)\n\0" as *const u8
                    as *const ::core::ffi::c_char,
                scan_no,
                (*prog).max_scans,
            );
            exit(EXIT_FAILURE);
        }
    }
    if (*prog).report != 0 {
        let mut total_passes: ::core::ffi::c_int =
            (*prog).pub_0.total_passes + (*prog).total_extra_passes;
        let mut percent_done: ::core::ffi::c_int =
            ((*prog).pub_0.pass_counter * 100 as ::core::ffi::c_long / (*prog).pub_0.pass_limit)
                as ::core::ffi::c_int;
        if percent_done != (*prog).percent_done {
            (*prog).percent_done = percent_done;
            if total_passes > 1 as ::core::ffi::c_int {
                fprintf(
                    stderr,
                    b"\rPass %d/%d: %3d%% \0" as *const u8 as *const ::core::ffi::c_char,
                    (*prog).pub_0.completed_passes
                        + (*prog).completed_extra_passes
                        + 1 as ::core::ffi::c_int,
                    total_passes,
                    percent_done,
                );
            } else {
                fprintf(
                    stderr,
                    b"\r %3d%% \0" as *const u8 as *const ::core::ffi::c_char,
                    percent_done,
                );
            }
            fflush(stderr);
        }
    }
}
