#[no_mangle]
pub unsafe extern "C" fn keymatch(
    mut arg: *mut ::core::ffi::c_char,
    mut keyword: *const ::core::ffi::c_char,
    mut minchars: ::core::ffi::c_int,
) -> boolean {
    let mut ca: ::core::ffi::c_int = 0;
    let mut ck: ::core::ffi::c_int = 0;
    let mut nmatched: ::core::ffi::c_int = 0 as ::core::ffi::c_int;
    loop {
        let fresh0 = arg;
        arg = arg.offset(1);
        ca = *fresh0 as ::core::ffi::c_int;
        if !(ca != '\0' as i32) {
            break;
        }
        let fresh1 = keyword;
        keyword = keyword.offset(1);
        ck = *fresh1 as ::core::ffi::c_int;
        if ck == '\0' as i32 {
            return FALSE;
        }
        if *(*__ctype_b_loc()).offset(ca as isize) as ::core::ffi::c_int
            & _ISupper as ::core::ffi::c_int as ::core::ffi::c_ushort as ::core::ffi::c_int
            != 0
        {
            ca = tolower(ca);
        }
        if ca != ck {
            return FALSE;
        }
        nmatched += 1;
    }
    if nmatched < minchars {
        return FALSE;
    }
    return TRUE;
}
#[no_mangle]
pub unsafe extern "C" fn read_stdin() -> *mut FILE {
    let mut input_file: *mut FILE = stdin;
    return input_file;
}
#[no_mangle]
pub unsafe extern "C" fn write_stdout() -> *mut FILE {
    let mut output_file: *mut FILE = stdout;
    return output_file;
}
